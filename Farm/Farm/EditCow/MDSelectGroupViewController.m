#pragma mark - Table view delegate

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath {
    [[tableView visibleCells] makeObjectsPerformSelector:@selector(setAccessoryType:) withObject:@(UITableViewCellAccessoryNone)];
    [[tableView cellForRowAtIndexPath:indexPath] setAccessoryType:UITableViewCellAccessoryCheckmark];
    if (self.didSelectGroupAction != NULL) {
        self.didSelectGroupAction(indexPath.row);
    }
    UIPopoverController* popover = [self valueForKey:@"_popoverController"];
    if (popover) {
        [popover dismissPopoverAnimated:YES];
    }
}

